import SwiftUI

//VARIÁVEL

//Variáveis são valores ao longo do código que eu posso querer mudar em algum outro momento. Para isso, posso definir VAR - de variável, depois falar o tipo e no final seu valor. Exp:

var idade:Int = 20 //variável (valor mutável) do tipo int, ou seja, um valor inteiro, que é igual ao valor 20.

//CONSTANTE
//Constantes são valores que não podem ser mudados mais ao longo do código após ser definido pela primeira vez. Para isso, utilizo a denominação let

let nome:String = "Luana"
